public class Num1 {
    public static void main(String[] args) {
        /* int: 인티저 (정수형 변수 선언시 사용) */
        int a = 10;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
